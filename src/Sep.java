public class Sep {
    public static void main(String[] args) {
        for(int i = 0; i<= 5; i= i + 1){
            System.out.print(i + " | " + " ");
            for(int j = 0; j <= 5; j= j + 1){
                System.out.print(j + " ");
            }
            System.out.println('\n');
        };

    }
}
